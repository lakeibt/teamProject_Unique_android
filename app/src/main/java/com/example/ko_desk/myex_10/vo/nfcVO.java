package com.example.ko_desk.myex_10.vo;

public class nfcVO {
	
	private int num;
	private String pnum;
	private String tagintime;
	private String tagouttime;
	private String date;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getPnum() {
		return pnum;
	}
	public void setPnum(String pnum) {
		this.pnum = pnum;
	}
	public String getTagintime() {
		return tagintime;
	}
	public void setTagintime(String tagintime) {
		this.tagintime = tagintime;
	}
	public String getTagouttime() {
		return tagouttime;
	}
	public void setTagouttime(String tagouttime) {
		this.tagouttime = tagouttime;
	}
	public String getDate() { return date; }
	public void setDate(String date) { this.date = date; }
}
